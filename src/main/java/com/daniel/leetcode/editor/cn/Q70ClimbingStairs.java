      //leetcode submit region begin(Prohibit modification and deletion)
class Solution {
    int[] a;
    public int climbStairs(int n) {
        a = new int[n + 1];
        return fb(n);
    }

    private int fb(int n) {
        if (a[n] > 0) {
            return a[n];
        }
        if (n == 1 || n == 0) {
            return 1;
        }
        a[n] = fb(n - 1) + fb(n - 2);
        return a[n];
    }
}
